class _IrScreenState extends State<IrScreen> with SingleTickerProviderStateMixin {
  static const _irChannel = MethodChannel('com.example.irphone/ir');
  String? _sending;
  late TabController _tabController;

  @override
  void initState() {
    super.initState();
    _tabController = TabController(length: 2, vsync: this);
  }

  @override
  void dispose() {
    _tabController.dispose();
    super.dispose();
  }

  Future<void> _send(String pronto) async {
    setState(() => _sending = pronto);
    try {
      await _irChannel.invokeMethod('sendProntoHex', {'pronto': pronto});
    } on PlatformException catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text(e.message ?? 'Error al enviar IR')),
        );
      }
    } finally {
      if (mounted) setState(() => _sending = null);
    }
  }

  Widget _buildGrid(List<PixmobBtn> buttons) {
    return GridView.builder(
      padding: const EdgeInsets.all(16),
      gridDelegate: const SliverGridDelegateWithFixedCrossAxisCount(
        crossAxisCount: 5,
        crossAxisSpacing: 8,
        mainAxisSpacing: 8,
      ),
      itemCount: buttons.length,
      itemBuilder: (context, i) {
        final btn = buttons[i];
        final active = _sending == btn.pronto;
        return GestureDetector(
          onTap: _sending != null ? null : () => _send(btn.pronto),
          child: AnimatedContainer(
            duration: const Duration(milliseconds: 150),
            decoration: BoxDecoration(
              color: btn.color,
              borderRadius: BorderRadius.circular(8),
              border: Border.all(
                color: active ? Colors.white : Colors.white24,
                width: active ? 2 : 0.5,
              ),
              boxShadow: active
                  ? [BoxShadow(color: btn.color.withAlpha(200), blurRadius: 14, spreadRadius: 4)]
                  : null,
            ),
            child: active
                ? const Center(
                    child: SizedBox(
                      width: 16,
                      height: 16,
                      child: CircularProgressIndicator(strokeWidth: 2, color: Colors.white),
                    ),
                  )
                : null,
          ),
        );
      },
    );
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      backgroundColor: const Color(0xFF080810),
      appBar: AppBar(
        backgroundColor: const Color(0xFF080810),
        foregroundColor: Colors.white,
        title: const Text('PixMob IR', style: TextStyle(letterSpacing: 2)),
        elevation: 0,
        bottom: TabBar(
          controller: _tabController,
          indicatorColor: Colors.white,
          labelColor: Colors.white,
          unselectedLabelColor: Colors.white54,
          tabs: const [
            Tab(text: 'Solido'),
            Tab(text: 'Fade'),
          ],
        ),
      ),
      body: TabBarView(
        controller: _tabController,
        children: [
          _buildGrid(pixmobSolid),
          _buildGrid(pixmobFade),
        ],
      ),
    );
  }
}